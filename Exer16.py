
numero = int(input("Digite um número inteiro: "))

# Calcular o dobro, triplo e a raiz quadrada
dobro = numero * 2
triplo = numero * 3
raiz_quadrada = numero ** 0.5  # "ou use: math.sqrt(numero)" = fornece as funções matemáticas, como cálculo de seno, cosseno, logaritmos, e raiz quadrada.


print(f"\nO dobro de {numero} é: {dobro}")
print(f"O triplo de {numero} é: {triplo}")
print(f"A raiz quadrada de {numero} é: {raiz_quadrada:.2f}")


